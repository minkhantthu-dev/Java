package pfc.decision_project.ifcondition;

public class TestSwap {

	public static void main(String[] args) {
		int n1, n2, temp;
		n1=100;
		n2=500;
		System.out.println("Before Swap");
		System.out.println("n1= "+n1+" ,"+"n2= "+n2);
		temp=n1;
		n1=n2;
		n2=temp;
		System.out.println("After Swap");
		System.out.println("n1= "+n1+" ,"+"n2= "+n2);

	}

}
